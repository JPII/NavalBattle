package com.jpii.roketgamer.achievement;

public class Achievement {
	private int id;
	
	/**
	 * Create a new <code>Achievement</code>
	 * @param id
	 */
	public Achievement(int id) {
		this.id = id;
	}
	
	/**
	 * Get <code>Achievement</code> ID
	 * @return id
	 */
	public int getID() {
		return id;
	}
}
