package com.jpii.roketgamer.leaderboard;

public class Leaderboard {
	private int id;
	
	/**
	 * Create a new <code>Leaderboard</code>
	 * @param id
	 */
	public Leaderboard(int id) {
		this.id = id;
	}
	
	/**
	 * Get <code>Leaderboard</code> ID
	 * @return
	 */
	public int getID() {
		return id;
	}
}
